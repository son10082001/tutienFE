function PasswordInput({ className, isWhite = false, ...props }: PasswordInputProps) {
  const [showPassword, setShowPassword] = React.useState(false);

  const togglePasswordVisibility = () => {
    if (props.disabled) return;
    setShowPassword(!showPassword);
  };

  return (
    <div className='relative'>
      <Input
        type={showPassword ? 'text' : 'password'}
        preIcon={!props.hideIcon ? <LockIcon /> : undefined}
        className={cn('pr-[38px]', className)}
        isWhite={isWhite}
        {...props}
      />
      <button
        type='button'
        onClick={togglePasswordVisibility}
        tabIndex={-1}
        className='-translate-y-1/2 absolute top-1/2 right-3 text-tx-soft-400 transition-colors hover:text-foreground focus:text-foreground focus:outline-none'
        aria-label={showPassword ? 'Hide password' : 'Show password'}
      >
        {showPassword ? <EyeIcon className='h-5 w-5' /> : <EyeOffIcon className='h-5 w-5' />}
      </button>
    </div>
  );
}

